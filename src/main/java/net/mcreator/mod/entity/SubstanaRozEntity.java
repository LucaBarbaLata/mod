	private boolean lastloop;
	private long lastSwing;
	public String animationprocedure = "empty";

	public SubstanaRozEntity(PlayMessages.SpawnEntity packet, Level world) {
		this(ModModEntities.SUBSTANA_ROZ.get(), world);
	}

	public SubstanaRozEntity(EntityType<SubstanaRozEntity> type, Level world) {
		super(type, world);
		xpReward = 0;
		setNoAi(false);
		this.setPathfindingMalus(BlockPathTypes.WATER, 0);
		this.moveControl = new MoveControl(this) {
			@Override
			public void tick() {
				if (SubstanaRozEntity.this.isInWater())
					SubstanaRozEntity.this.setDeltaMovement(SubstanaRozEntity.this.getDeltaMovement().add(0, 0.005, 0));
				if (this.operation == MoveControl.Operation.MOVE_TO && !SubstanaRozEntity.this.getNavigation().isDone()) {
					double dx = this.wantedX - SubstanaRozEntity.this.getX();
					double dy = this.wantedY - SubstanaRozEntity.this.getY();
					double dz = this.wantedZ - SubstanaRozEntity.this.getZ();
					float f = (float) (Mth.atan2(dz, dx) * (double) (180 / Math.PI)) - 90;
					float f1 = (float) (this.speedModifier * SubstanaRozEntity.this.getAttribute(Attributes.MOVEMENT_SPEED).getValue());
					SubstanaRozEntity.this.setYRot(this.rotlerp(SubstanaRozEntity.this.getYRot(), f, 10));
					SubstanaRozEntity.this.yBodyRot = SubstanaRozEntity.this.getYRot();
					SubstanaRozEntity.this.yHeadRot = SubstanaRozEntity.this.getYRot();
					if (SubstanaRozEntity.this.isInWater()) {
						SubstanaRozEntity.this.setSpeed((float) SubstanaRozEntity.this.getAttribute(Attributes.MOVEMENT_SPEED).getValue());
						float f2 = -(float) (Mth.atan2(dy, (float) Math.sqrt(dx * dx + dz * dz)) * (180 / Math.PI));
						f2 = Mth.clamp(Mth.wrapDegrees(f2), -85, 85);
						SubstanaRozEntity.this.setXRot(this.rotlerp(SubstanaRozEntity.this.getXRot(), f2, 5));
						float f3 = Mth.cos(SubstanaRozEntity.this.getXRot() * (float) (Math.PI / 180.0));
						SubstanaRozEntity.this.setZza(f3 * f1);
						SubstanaRozEntity.this.setYya((float) (f1 * dy));
					} else {
						SubstanaRozEntity.this.setSpeed(f1 * 0.05F);
					}
				} else {
					SubstanaRozEntity.this.setSpeed(0);
					SubstanaRozEntity.this.setYya(0);
					SubstanaRozEntity.this.setZza(0);
				}
			}
		};
	}

	@Override
	protected void defineSynchedData() {
		super.defineSynchedData();
		this.entityData.define(SHOOT, false);
		this.entityData.define(ANIMATION, "undefined");
		this.entityData.define(TEXTURE, "subsroztexture");
	}

	public void setTexture(String texture) {
		this.entityData.set(TEXTURE, texture);
	}

	public String getTexture() {
		return this.entityData.get(TEXTURE);
	}

