	public static final EntityDataAccessor<String> ANIMATION = SynchedEntityData.defineId(SubstanaRozEntity.class, EntityDataSerializers.STRING);
	public static final EntityDataAccessor<String> TEXTURE = SynchedEntityData.defineId(SubstanaRozEntity.class, EntityDataSerializers.STRING);
